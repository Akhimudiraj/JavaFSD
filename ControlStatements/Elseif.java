public class Elseif {
    public static void main(String[] args) {
        int a=101,b=101;
        if(a<b){
            System.out.println("a is smaller ");
        }else if(a>b){
            System.out.println("a is greater");
        }else if(a==b){
            System.out.println("equal");
        }else{
            System.out.println("not equal");
        }
    }
    
}
