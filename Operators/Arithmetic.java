public class Arithmetic {
    public static void main(String[] args) {
        int a=10,b=20;
        int sum=a+b;
        int sub=a-b;
        float mul=a*b;
        double div=a/b;
        float mod=a%b;

        System.out.println("sum "+ sum);
        System.out.println("sub"+sub);
        System.out.println("mul"+mul);
        System.out.println("div"+div);
        System.out.println("mod"+mod);
    }

    
}
